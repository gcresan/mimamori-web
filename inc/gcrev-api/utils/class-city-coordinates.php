    /**
     * 市区町村名から中心座標を取得する。
     *
     * @param string $city_name 市区町村名（例: '松山市'）
     * @return array{lat: float, lng: float, label: string}|null
     */
    public static function get( string $city_name ): ?array {
        $city_name = trim( $city_name );
        if ( $city_name === '' ) {
            return null;
        }
        return self::COORDINATES[ $city_name ] ?? null;
    }

    /**
     * ユーザーのクライアント設定（商圏）から市区町村中心座標を自動検出する。
     *
     * gcrev_client_area_type が 'city' の場合に gcrev_client_area_city を参照し、
     * マッピングに存在する最初の市区町村の中心座標を返す。
     * area_city はカンマ区切りで複数指定可（例: '渋谷区, 新宿区'）。
     *
     * @param int $user_id WordPress ユーザーID
     * @return array{lat: float, lng: float, label: string}|null
     */
    public static function get_for_user( int $user_id ): ?array {
        if ( ! function_exists( 'gcrev_get_client_settings' ) ) {
            return null;
        }

        $settings  = gcrev_get_client_settings( $user_id );
        $area_type = $settings['area_type'] ?? '';

        // 'city' タイプの場合のみ自動検出
        if ( $area_type !== 'city' ) {
            return null;
        }

        $city_raw = $settings['area_city'] ?? '';
        if ( $city_raw === '' ) {
            return null;
        }

        // カンマ区切りで複数市区町村が指定されている場合、最初にマッチしたものを使用
        $cities = array_map( 'trim', explode( ',', $city_raw ) );

        foreach ( $cities as $city ) {
            $coords = self::get( $city );
            if ( $coords !== null ) {
                return $coords;
            }
        }

        return null;
    }

    /**
     * ユーザーに対して自動検出が可能かどうかを判定する。
     *
     * @param int $user_id WordPress ユーザーID
     * @return bool
     */
    public static function has_auto_coordinates( int $user_id ): bool {
        return self::get_for_user( $user_id ) !== null;
    }

    /**
     * マッピングに登録されている全市区町村名を返す（管理画面等で利用可能）。
     *
     * @return string[]
     */
    public static function get_available_cities(): array {
        return array_keys( self::COORDINATES );
    }
